
package daniel_arrays;

public class Pessoas {
  int idade;
  String nome;
  String cargo;

    public Pessoas(int idade, String nome, String cargo) {
        this.idade = idade;
        this.nome = nome;
        this.cargo = cargo;
    }

    public int getIdade() {
        return idade;
    }

    public String getNome() {
        return nome;
    }

    public String getCargo() {
        return cargo;
    }
    
    
  
  
}
